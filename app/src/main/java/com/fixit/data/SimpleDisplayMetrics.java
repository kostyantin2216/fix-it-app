public class SimpleDisplayMetrics {

    private final int width;
    private final int height;

    public SimpleDisplayMetrics(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }


    public int getHeight() {
        return height;
    }

}
